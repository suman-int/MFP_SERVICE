SELECT 	DISTINCT PRSN_ID_CD, STATUS_CD, PRSN_TYPE_CD, FRST_NM, MIDL_NM, LAST_NM, JOB_CD, LOCTN_CD, USERID, EMAIL_ADDR, 
		REGION_CD RGN_CD, ZONE_CD, DISTRICT_CD, TYPE_CD, JOB_TITLE_TX	
FROM 	(
		SELECT 	DISTINCT A.PRSN_ID_CD, A.STATUS_CD, A.PRSN_TYPE_CD, A.FRST_NM, A.MIDL_NM, A.LAST_NM, B.JOB_CD, B.LOCTN_CD, C.USERID, C.EMAIL_ADDR, 
		(CASE WHEN SUBSTR(B.LOCTN_CD, 1, 2) = 'SE' THEN 'GU' ELSE SUBSTR(B.LOCTN_CD, 1, 2) END )  REGION_CD, E.ZONE_CD ZONE_CD, SUBSTR(B.LOCTN_CD, 4, 2) DISTRICT_CD, SUBSTR(B.LOCTN_CD, 3, 1) TYPE_CD, D.JOB_TITLE_TX    
		FROM 	MMAS.BTC03020 A,
				MMAS.BTC03050 B,
				MMAS.BTC03110 C,
				MMAS.BTC03010 D,
				(
				SELECT  DISTINCT RGN_CD, TYPE_CD, ZONE_CD, DSTRCT_CD, to_char(EFF_END_DT, 'YYYY-MM-DD')
						FROM MMAS.BTC02090 
						WHERE TYPE_CD = 'V'
						AND to_char(EFF_END_DT, 'YYYY-MM-DD') = '0001-01-01'
				) E
		WHERE   A.PRSN_ID_CD = B.PRSN_ID_CD 
		AND 	A.PRSN_ID_CD = C.PRSN_ID_CD 
		AND 	E.RGN_CD = (CASE WHEN SUBSTR(B.LOCTN_CD, 1, 2) = 'SE' THEN 'GU' ELSE SUBSTR(B.LOCTN_CD, 1, 2) END )
		AND 	E.DSTRCT_CD = SUBSTR(B.LOCTN_CD, 4, 2)
		AND 	D.JOB_CD = B.JOB_CD 
		AND		PRSN_TYPE_CD = 'M'
		AND 	C.END_DT = '0001-01-01'
		AND 	B.JOB_CD IN ('MB11', 'MC11', 'MD11', 'ME11', 'MO11', 'MP11')
		UNION
		SELECT 	DISTINCT A.PRSN_ID_CD, A.STATUS_CD, A.PRSN_TYPE_CD, A.FRST_NM, A.MIDL_NM, A.LAST_NM, B.JOB_CD, B.LOCTN_CD, C.USERID, C.EMAIL_ADDR, 
		(CASE WHEN SUBSTR(B.LOCTN_CD, 1, 2) = 'SE' THEN 'GU' ELSE SUBSTR(B.LOCTN_CD, 1, 2) END ) REGION_CD, SUBSTR(B.LOCTN_CD, 4, 2) ZONE_CD, ' ' DISTRICT_CD, SUBSTR(B.LOCTN_CD, 3, 1) TYPE_CD, D.JOB_TITLE_TX    
		FROM 	MMAS.BTC03020 A,
				MMAS.BTC03050 B,
				MMAS.BTC03110 C,
				MMAS.BTC03010 D 
		WHERE   A.PRSN_ID_CD = B.PRSN_ID_CD 
		AND 	A.PRSN_ID_CD = C.PRSN_ID_CD 
		AND 	D.JOB_CD = B.JOB_CD 
		AND		PRSN_TYPE_CD = 'M'
		AND 	C.END_DT = '0001-01-01'
		AND 	B.JOB_CD = 'MZ11'
		UNION
		SELECT 	DISTINCT A.PRSN_ID_CD, A.STATUS_CD, A.PRSN_TYPE_CD, A.FRST_NM, A.MIDL_NM, A.LAST_NM, B.JOB_CD, B.LOCTN_CD, C.USERID, C.EMAIL_ADDR, 
		' ' REGION_CD, ' ' ZONE_CD, ' ' DISTRICT_CD, ' ' TYPE_CD, D.JOB_TITLE_TX
		FROM 	MMAS.BTC03020 A,
				MMAS.BTC03050 B,
				MMAS.BTC03110 C,
				MMAS.BTC03010 D
		WHERE   A.PRSN_ID_CD = B.PRSN_ID_CD
		AND 	A.PRSN_ID_CD = C.PRSN_ID_CD
		AND 	D.JOB_CD = B.JOB_CD
		AND		PRSN_TYPE_CD = 'M'
		AND 	C.END_DT = '0001-01-01'
		AND 	B.LOCTN_CD = 'MA92'
		) X 
		WHERE USERID IN ('TMOYERS3',
						'SMORRI18',
						'JEDWAR29 ',
						'WGARBUTT',
						'RMOZDZEN',
						'MMISASI',
						'CVENDITT',
						'BMULLEN3',
						'JINFOSIN',
						'TEICHENA',
						'SAMRANI',
						'PHUFFORD')
		ORDER BY JOB_CD 

