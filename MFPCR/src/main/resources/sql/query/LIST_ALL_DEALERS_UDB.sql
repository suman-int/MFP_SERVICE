SELECT
		A.DBA_NM,
		A.STATUS_CD,
		A.STATUS_DT,
		B.RGN_CD,
		A.CITY_NM,
		A.ZIP1_CD,
		A.CNTY_CD,
		A.ST_CD,
		A.MDA_CD,
		A.SOA_NM,
		A.APPT_DT,
		A.TERM_DT,
		A.PREV_DLR_CD,
		A.NXT_DLR_CD,
		A.TIME_ZONE_CD,
		A.SOA_CD,
		A.USED_CAR_FL,
		A.CNTRY_CD,
		A.ZIP2_CD,
		A.DLR_INACTV_DT,
		A.SVC_ONLY_FL,
		A.SVC_ONLY_DT,
		B.ZONE_CD,
		B.DSTRCT_CD ,
		A.FCLTY_TYPE_CD,
		A.DLR_CD
FROM
	$SCHEMA$BTC02010 A ,
	$SCHEMA$BTC02090 B
WHERE
	A.DLR_CD = B.DLR_CD
AND
	B.TYPE_CD = 'V'
AND 
	B.EFF_END_DT = '0001-01-01'
AND
	B.RGN_CD IN ('GU', 'MW', 'NE', 'PA')
AND
	B.DSTRCT_CD   <> '99'
AND
	B.ZONE_CD  <> '99'
AND
	A.STATUS_CD  = 'A'
