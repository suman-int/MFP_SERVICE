DROP VIEW IF EXISTS V_RETAIL_INVENTORY_BY_DEALER
;
SELECT
	DLR_CD,
	SALES_YYMM,
	TRANSACTION_TYPE,
	CNT,
	QTY,
	SALES_MO_TOTAL_SELL_DAYS,
	CAL_MO_FIRST_DAY,
	CAL_MO_LAST_DAY,
	DSR
FROM $SCHEMA$FLAT_RETAIL_SALES_MONTH_DSR
WHERE    TRANSACTION_TYPE = 'Retail'
	AND  SALES_YYMM BETWEEN TO_NUMBER(TO_CHAR(ADD_MONTHS(?, -3), 'YYYYMM'))
	AND  TO_NUMBER(TO_CHAR(ADD_MONTHS(?, -1), 'YYYYMM'))

